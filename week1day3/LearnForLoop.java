package week1day3;

public class LearnForLoop {

	public static void main(String[] args) {
//print even number btw 11-20
		
		for(int i=11; i<=20; i++)
			
			if(i%2==0)
				
		{
				System.out.println(i);
			
		}
		
		//reverse loop
		
		for(int i=5;i>=0; i--)
			if(i%2==1)
		{
			System.out.println(i);
		}
	}

}
